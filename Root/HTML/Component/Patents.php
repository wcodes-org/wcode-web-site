	<h2>Conflict</h2>
	<p>
		A few years later I found out that a startup named “<strong><em>What3Words</em></strong>” in UK had applied for a patent on the same invention, with the only apparent difference being that their code is shorter with only 3 words while WCode had 5; But the disparity was primarily because WCode used a dictionary of only <strong class='blue'>1024</strong> simple words against the many thousands.<br>
		The filing date for the patent application: <a class='content-link' href=''>GB1307148.5</a> was April 19th - 2013, only <strong>8 days</strong> later after my campaign went live.
	</p>
	<div class='top-bottom-gap'>
		<?php $img_title='patent_application_uk_ipo-GB1307148.5'; $ext='jpg'; $alt="W3W patent application, UK-IPO, GB1307148.5"; $center='true'; $max_height='true'; require('Fragment\Component_image.php') ?>
	</div>
	<p>
		Since I had invented and showcased and also published the binary for encoding geo-coordinates into 5 words (from a limited dictionary of only 1024 words), I tried to get the patent application invalidated.<br>
		Thus I sought help on StackExchange Patents <a class='content-link' href='https://patents.stackexchange.com/questions/13629/i-had-invented-and-published-before-this-patent-application-how-do-i-get-it-in'>Q&A site</a>.
	</p>
	<p>
		I tried submitting a third party observation but was unable to since the priority date is past 28 months limit.<br>
		And later tried contacting the UK-IPO office with the same data through email.
	</p>
