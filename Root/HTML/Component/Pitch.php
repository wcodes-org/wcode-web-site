	<h2>How is it useful?</h2>
	<p>
		When asked for your telephone no. wouldn't it be nice if you had to say only four words instead of the usual ten. For example:<br>
		"<strong><em>Cat Apple Mango Tomato</em></strong>" against "<strong><em>Nine Eight Zero One Two Three Four Five Six Seven</em></strong>".<br><br>
		Also the chances of making mistakes are reduced since only carefully handpicked words that are simple and unambiguous and have distinct pronunciation are used.<br><br>
		Additionally check-summed WCode can be used to ensure correct delivery.
	</p>
